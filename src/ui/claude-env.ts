export const CLAUDE_ENV_FIXED = CLAUDE_CODE_ENV_CONFIG.lockedEnv

export const CLAUDE_MODEL_ENV_KEYS = Object.keys(CLAUDE_CODE_ENV_CONFIG.editableEnvDefaults) as ClaudeCodeEditableEnvKey[]

/** All canEdit keys from export-env-claude config (model keys + extra editable keys like CLAUDE_CODE_DISABLE_1M_CONTEXT) */
export const EXPORT_ENV_CAN_EDIT_KEYS = Object.keys(exportEnvClaudeConfig.codex.canEdit) as Array<keyof ExportEnvClaudeCodeConfig["codex"]["canEdit"]>

/** Extra canEdit keys that are not model keys (stored in draft.extraEnv) */
export const EXPORT_ENV_EXTRA_EDITABLE_KEYS = EXPORT_ENV_CAN_EDIT_KEYS.filter(
  (key) => !(CLAUDE_MODEL_ENV_KEYS as readonly string[]).includes(key),
)

/** All editable keys in order: model keys first, then extra canEdit keys */
export const ALL_EDITABLE_KEYS: readonly string[] = [...CLAUDE_MODEL_ENV_KEYS, ...EXPORT_ENV_EXTRA_EDITABLE_KEYS]

/** Static keys from export-env-claude config, excluding ANTHROPIC_BASE_URL (auto-generated by system) */
export const EXPORT_ENV_STATIC_KEYS = Object.keys(exportEnvClaudeConfig.codex.static).filter(
  (key) => key !== "ANTHROPIC_BASE_URL",
) as string[]

/**
 * Static key-value pairs for display (excluding ANTHROPIC_BASE_URL).
 * Auth-related keys (ANTHROPIC_AUTH_TOKEN, ANTHROPIC_API_KEY) are sourced from
 * CLAUDE_ENV_FIXED so that the displayed values always match what gets persisted
 * via managedEnvironmentEntries(). Other static keys fall back to export-env-claude config.
 */
export const EXPORT_ENV_STATIC_ENTRIES: Array<{ key: string; value: string }> = EXPORT_ENV_STATIC_KEYS.map((key) => ({
  key,
  value: (CLAUDE_ENV_FIXED as Record<string, string>)[key] ?? (exportEnvClaudeConfig.codex.static as Record<string, string>)[key],
}))

export const CLAUDE_ENV_KEYS = [
  "ANTHROPIC_BASE_URL",
  ...Object.keys(CLAUDE_ENV_FIXED),
  ...CLAUDE_MODEL_ENV_KEYS,
] as const

export type ClaudeModelEnvKey = ClaudeCodeEditableEnvKey
export interface ClaudeEnvironmentDraft extends Record<ClaudeModelEnvKey, string> {
  extraEnv: Record<string, string>
  unsetEnv: string[]
}
export type ClaudeSettingsScope = "user" | "project" | "local"
export type ShellKind = "posix" | "powershell"
export type ShellDetection = { kind: ShellKind; name: string } | { kind: "unsupported"; name: string; reason: string }
export interface ClaudeEnvironmentRunOptions {
  authFile?: string
  persist?: boolean
  settingsFile?: string
  apiPassword?: string
}

interface ClaudeSettingsFile {
  env?: Record<string, unknown>
  [key: string]: unknown
}

export function exportEnvConfigForProvider(providerMode: ProviderMode = "codex") {
  return exportEnvClaudeConfig[providerMode] ?? exportEnvClaudeConfig.codex
}

export function defaultClaudeEnvironment(providerMode: ProviderMode = "codex"): ClaudeEnvironmentDraft {
  const defaults = modelEnvDefaultsForProvider(providerMode)
  return {
    ANTHROPIC_MODEL: modelEnvValue("ANTHROPIC_MODEL", defaults.ANTHROPIC_MODEL, providerMode),
    ANTHROPIC_DEFAULT_OPUS_MODEL: modelEnvValue("ANTHROPIC_DEFAULT_OPUS_MODEL", defaults.ANTHROPIC_DEFAULT_OPUS_MODEL, providerMode),
    ANTHROPIC_DEFAULT_SONNET_MODEL: modelEnvValue("ANTHROPIC_DEFAULT_SONNET_MODEL", defaults.ANTHROPIC_DEFAULT_SONNET_MODEL, providerMode),
    ANTHROPIC_DEFAULT_HAIKU_MODEL: modelEnvValue("ANTHROPIC_DEFAULT_HAIKU_MODEL", defaults.ANTHROPIC_DEFAULT_HAIKU_MODEL, providerMode),
    extraEnv: extraEnvDefaultsForProvider(providerMode),
    unsetEnv: [...CLAUDE_CODE_ENV_CONFIG.defaultUnsetEnv],
  }
}

export function claudeEnvironmentConfigPath(authFile: string) {
  return path.join(path.dirname(authFile), ".claude-env.json")
}

export function claudeSettingsPath(file?: string) {
  return file ?? path.join(homeDir(), ".claude", "settings.json")
}

export function claudeSettingsPathForScope(scope: ClaudeSettingsScope, cwd = process.cwd()) {
  if (scope === "project") return path.join(cwd, ".claude", "settings.json")
  if (scope === "local") return path.join(cwd, ".claude", "settings.local.json")
  return claudeSettingsPath()
}

export function claudeSettingsScopeLabel(scope: ClaudeSettingsScope) {
  if (scope === "project") return ".claude/settings.json"
  if (scope === "local") return ".claude/settings.local.json"
  return "~/.claude/settings.json"
}

